package fr.esisar.calculatrice;

/**
 * Classe contenant les méthodes d'opérations arithmétiques 
 */
public class Calculatrice {



/**
 * 	
 * @param operande1 Entier représentant le  terme de droite
 * @param operande2 Entier représentant le terme de gauche
 * @return Entier représentant le résultat
 */
public int ajouter(int operande1, int operande2) {
	
	return operande1+operande2;	

}

/**
 * 	
 * @param operande1 Entier représentant le  terme de droite
 * @param operande2 Entier représentant le terme de gauche
 * @return Entier représentant le résultat
 */
public int soustraire(int operande1, int operande2) {
	
	return operande1-operande2;	

}

/**
 * 	
 * @param operande1 Entier représentant le  terme de droite
 * @param operande2 Entier représentant le terme de gauche
 * @return Entier représentant le résultat
 */
public int multiplier(int operande1, int operande2) {
	
	return operande1*operande2;	

}

/**
 * 	
 * @param operande1 Entier représentant le  terme de droite
 * @param operande2 Entier représentant le terme de gauche
 * @return Entier représentant le résultat
 */
public int diviser(int operande1, int operande2) {
	
return operande1/operande2;	

}





	
}
